      private boolean check() {
    String oldCategory = jTextField3.getText();
    String newCategory = jTextField3.getText(); 
    
    

    // Check if both email and phone match the existing values (value[2] and value[4])
    if (newCategory.equals(oldCategory)) {
        return false;  // No changes made to email and phone
    } else {
         if (!newCategory.equals(oldCategory)) {
            boolean x = cat.isCategoryNameExist(newCategory);
            if (x) {
                JOptionPane.showMessageDialog(this, "This Category Name already exists", "Warning", JOptionPane.WARNING_MESSAGE);
            }
            return x;  // Return whether the email exists
       }
    }
    
    return false;  // No issues, everything is fine
}
