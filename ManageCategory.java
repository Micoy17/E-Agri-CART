    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
      if (isEmpty()) {
    // Step 1: Get the inputs
    int ID = Integer.parseInt(jTextField2.getText());  // Parsing the category ID
    String cname = jTextField3.getText().trim();  // Getting the category name (trim to avoid extra spaces)
    String desc = jTextField4.getText().trim();  // Getting the description (trim to avoid extra spaces)

    // Step 2: Check if Category ID already exists
    if (cat.isIdExist(ID)) {
        // Category ID already exists
        JOptionPane.showMessageDialog(this, "Category ID is already exist", "Warning", JOptionPane.WARNING_MESSAGE);
    } else {
        // Step 3: Check if Category Name already exists
        if (!cat.isCategoryNameExist(cname)) {
            // Step 4: Insert the category
            cat.insert(ID, cname, desc);

            // Step 5: Update the table
            jTable2.setModel(new DefaultTableModel(null, new Object[]{"Category ID", "Category Name", "Category Description"}));

            // Step 6: Fetch updated category values
            cat.getCategoryValue(jTable2, "");

            // Step 7: Clear the input fields
            clear();
        } else {
            // Category Name already exists
            JOptionPane.showMessageDialog(this, "Category Name is already exist", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }
}
    }//GEN-LAST:event_addActionPerformed

