    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        if (isEmpty()) {
            int ID = Integer.parseInt(jTextField2.getText());
            if (cat.isIdExist(ID)) {
                if (!check()) {
                    String cname = jTextField3.getText();
                    String desc = jTextField4.getText();
                    cat.update(ID, cname, desc);
                    jTable2.setModel(new DefaultTableModel(null, new Object[]{"Category ID", "Category Name", "Category Description"}));
                    cat.getCategoryValue(jTable2, "");
                    clear();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Category ID is doesn't exist", "Warning", 2);
            }
        }
    }//GEN-LAST:event_updateActionPerformed

