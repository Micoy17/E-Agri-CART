       private boolean check() {
           String newProduct =jTextField3.getText();
           String oldProduct =model.getValueAt(rowIndex,1).toString(); 
    String oldCategory = jComboBox1.getSelectedItem().toString();
    String newCategory = model.getValueAt(rowIndex,2).toString(); 
    
    

    // Check if both email and phone match the existing values (value[2] and value[4])
    if (newProduct.equals(oldProduct)&& newCategory.equals(oldCategory)) {
        return false;  // No changes made to email and phone
    } else {
         if (!newCategory.equals(oldCategory)) {
            boolean x = product.isProCatExist(newProduct, newCategory);
            if (x) {
                JOptionPane.showMessageDialog(this, "Product Name and Category  already exists", "Warning", 2);
                return x;
            }
       }
    }
    
    return false;  // No issues, everything is fine
}
    private boolean isNumeric(String s){
    try{
    double d = Double.parseDouble(s);
    return true;
    }
    catch(NumberFormatException ex){
        JOptionPane.showMessageDialog(this,""+ex+"\nNumeric Value is required for the quantity and price field");
    }
    return false;
    }
