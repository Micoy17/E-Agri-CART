    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
    if (isEmpty()) {
    // Parse the fields to get values
    int id = Integer.parseInt(jTextField2.getText());
    String pname = jTextField3.getText();
    String cat = jComboBox1.getSelectedItem().toString();
    int qty = Integer.parseInt(jTextField4.getText());

    // Validate price is numeric
    if (isNumeric(jTextField5.getText())) {
        double price = Double.parseDouble(jTextField5.getText());

        // Skip the ID existence check, as we are updating an existing product
        if (!check()) {  // Custom validation (if any)
            // Call the update method with correct parameters
            product.update(id, pname, cat, qty, price);

            // Refresh the table with updated values
            jTable2.setModel(new DefaultTableModel(null, new Object[]{"Product ID", "Product Name", "Category", "Quantity", "Price"}));
            product.getProductValue(jTable2, "");

            // Clear input fields
            clear();
        }
    } else {
        JOptionPane.showMessageDialog(this, "Invalid price format", "Warning", JOptionPane.WARNING_MESSAGE);
    }
}

    }//GEN-LAST:event_updateActionPerformed

    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if (isEmpty()) {
            int id = Integer.parseInt(jTextField2.getText());
            String pname = jTextField3.getText();
            String cat = jComboBox1.getSelectedItem().toString();
            int qty = Integer.parseInt(jTextField4.getText());
            if (isNumeric(jTextField5.getText())) {
                double price = Double.parseDouble(jTextField5.getText());
                if (!product.isIdExist(id)) {
                    if (!product.isProCatExist(pname, cat)) {
                        product.insert(id, pname, cat, qty, price);
                        jTable2.setModel(new DefaultTableModel(null, new Object[]{"Product ID", "Product Name", "Category Name ", "Quantity", "Price"}));
                        product.getProductValue(jTable2, "");
                        clear();
                    } else {
                        JOptionPane.showMessageDialog(this, "Product and Category is already exist", "Warning", 2);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Product id is already exist", "Warning", 2);
                }
            }

        }
    }//GEN-LAST:event_addActionPerformed

